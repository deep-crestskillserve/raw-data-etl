"""
Event-wise table mappings organized by phase.

This module provides mappings from event types to database tables with their
corresponding JSON field paths, organized by processing phase.
"""

from typing import Dict, List, Optional, Any
from table_phase_mapping import get_table_phase




def get_event_table_mappings(event_type: str, table_name: str) -> Optional[Dict[str, str]]:
    """
    Get field mappings for a specific event type and table name.
    
    Args:
        event_type: The event type (e.g., 'reservationCreate', 'groupCreate')
        table_name: The database table name (e.g., 'hotel', 'reservation')
    
    Returns:
        Dictionary mapping JSON field paths to table column names, or None if not found
    """
    print("--------------------------------")
    print("in get_event_table_mappings")
    print(f"event type: {event_type}")
    print(f"table name: {table_name}")

    # Normalize event type (handle aliases)
    event_type = normalize_event_type(event_type)
    
    if event_type not in EVENT_TABLE_MAPPINGS:
        return None
    
    # Get the phase for this table
    table_phase = get_table_phase(table_name)
    print(f"table phase: {table_phase}")

    event_mappings = EVENT_TABLE_MAPPINGS[event_type]

    # handling case where a table is in multiple phases because multiple path for the same table are present (e.g. confirmationNumber is present in phase1 and phase2 for reservationCheckOut event)

    if isinstance(table_phase, list):
        results = []
        for phase in table_phase:
            phase_data = event_mappings.get(phase)
            if phase_data and phase_data.get(table_name):
                results.append(phase_data.get(table_name))
        return results if results else None
    
    
    # Check if the phase exists for this event
    if not table_phase or table_phase not in event_mappings:
        return None
    
    # Get the table mappings for this phase
    table_mappings = event_mappings[table_phase].get(table_name)
    
    # Return the field mappings for this table
    return table_mappings


def normalize_event_type(event_type: str) -> str:
    """Normalize event type to handle aliases."""
    aliases = {
        'reservationModify': 'reservationCreate',
        'folioGuestIncrementalUpdate': 'folioGuestCreate',
        'folioGroupIncrementalUpdate': 'folioGroupCreate',
        'folioHouseAccountIncrementalUpdate': 'folioHouseAccountCreate',
        'invoiceIncrementalUpdate': 'invoiceCreate',
        'groupModify': 'groupCreate'
    }
    return aliases.get(event_type, event_type)


